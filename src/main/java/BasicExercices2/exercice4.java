/**
 * 4) Muestra los números del 1 al 100 (ambos incluidos), Usando un bucle while.
 */

package BasicExercices2;

public class exercice4 {

    public static void main(String[] args) {
        int a = 1;

        while (a <= 100){
            System.out.println("Número "+ a);
            a++;
        }
    }

}
