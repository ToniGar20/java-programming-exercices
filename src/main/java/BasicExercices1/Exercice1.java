/**
 * 1) Declara dos variables numéricas (con el valor que desees),
 * muestra por consola la suma, resta, multiplicación, división y módulo (resto de la división).
 */

package BasicExercices1;

public class Exercice1 {
    public static void main(String[] args) {
        int a = 7;
        int b = 5;

        System.out.println("La suma de a y b es " + (a + b));
        System.out.println("La resta de a y b es " + (a - b));
        System.out.println("La divisón de a y b es " + (a / b));
        System.out.println("El módulo de a y b es " + (a%b));
    }
}

